#include "meta/headers.hpp"


/***********************************************************************
     * Framer
     * constructor

***********************************************************************/
ach::Framer::Framer(float _timer)
{
	_timer = _timer;

	reset();
}



/***********************************************************************
     * Framer
     * destructor

***********************************************************************/
ach::Framer::~Framer()
{
}



/***********************************************************************
     * Framer
     * destructor

***********************************************************************/
void ach::Framer::update()
{
	value -= app->tm->frame;

	while (value < 0.0f)
	{
		value += timer;
		frame++;
	}
}



/***********************************************************************
     * Framer
     * reset

***********************************************************************/
void ach::Framer::reset()
{
	frame = 0;
	value = timer;
}
