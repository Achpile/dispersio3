#include "meta/headers.hpp"


/***********************************************************************
     * Timer
     * constructor

***********************************************************************/
ach::Timer::Timer(float _timer)
{
	set(_timer);
}



/***********************************************************************
     * Timer
     * destructor

***********************************************************************/
ach::Timer::~Timer()
{
}



/***********************************************************************
     * Timer
     * set

***********************************************************************/
void ach::Timer::set(float _timer)
{
	timer = _timer;
	value = _timer;
}



/***********************************************************************
     * Timer
     * reset

***********************************************************************/
void ach::Timer::reset()
{
	value = timer;
}



/***********************************************************************
     * Timer
     * process

***********************************************************************/
bool ach::Timer::process()
{
	if (value <= 0)
		return false;

	value -= tm->frame;

	if (value <= 0)
	{
		value = 0;
	}

	return value > 0.0f;
}



/***********************************************************************
     * Timer
     * isActive

***********************************************************************/
bool ach::Timer::isActive()
{
	return value > 0.0f;
}
