#include "meta/headers.hpp"


/***********************************************************************
     * flag_set

***********************************************************************/
void flag_set(unsigned int *value, int bit)
{
	(*value) |= (1 << bit);
}



/***********************************************************************
     * flag_unset

***********************************************************************/
void flag_unset(unsigned int *value, int bit)
{
	(*value) &= ~(1 << bit);
}



/***********************************************************************
     * flag_toggle

***********************************************************************/
void flag_toggle(unsigned int *value, int bit)
{
	(*value) ^= (1 << bit);
}



/***********************************************************************
     * flag_check

***********************************************************************/
bool flag_check(unsigned int  value, int bit)
{
	return (value & (1 << bit));
}
