#include "meta/headers.hpp"


/***********************************************************************
     * html_start

***********************************************************************/
void html_start(FILE *fp)
{
	fprintf(fp, "<!DOCTYPE HTML>\n");
	fprintf(fp, "<HTML>\n");
}



/***********************************************************************
     * html_head_start

***********************************************************************/
void html_head_start(FILE *fp)
{
	fprintf(fp, "<HEAD>\n");
	fprintf(fp, "<META content='text/html; charset=UTF-8' http-equiv='content-type'>\n");
}



/***********************************************************************
     * html_body_start

***********************************************************************/
void html_body_start(FILE *fp)
{
	fprintf(fp, "<BODY>\n");
}



/***********************************************************************
     * html_end

***********************************************************************/
void html_end(FILE *fp)
{
	fprintf(fp, "</HTML>\n");
}



/***********************************************************************
     * html_head_end

***********************************************************************/
void html_head_end(FILE *fp)
{
	fprintf(fp, "</HEAD>\n");
}



/***********************************************************************
     * html_body_end

***********************************************************************/
void html_body_end(FILE *fp)
{
	fprintf(fp, "</BODY>\n");
}



/***********************************************************************
     * html_title

***********************************************************************/
void html_title(FILE *fp, const char *title)
{
	fprintf(fp, "<TITLE>%s</TITLE>\n", title);
}
