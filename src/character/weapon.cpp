#include "meta/headers.hpp"


/***********************************************************************
     * Weapon
     * constructor

***********************************************************************/
ach::Weapon::Weapon(ach::DataWeapon *_base)
{
	base = _base;

	cooldown.set(base->cooldown);
	cooldown.zero();
}



/***********************************************************************
     * Weapon
     * destructor

***********************************************************************/
ach::Weapon::~Weapon()
{
}



/***********************************************************************
     * Weapon
     * update

***********************************************************************/
void ach::Weapon::update()
{
	cooldown.update();
}



/***********************************************************************
     * Weapon
     * shot

***********************************************************************/
void ach::Weapon::shot()
{
	if (cooldown.isActive())
		return;

	cooldown.reset();
	sm->play(base->shot);
}
